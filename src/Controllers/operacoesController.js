const soma = (n1, n2) => {
    const result = n1 + n2;

    return result
}

const sub = (n1, n2) => {
    const result = n1 - n2;

    return result
}


const mult = (n1, n2) => {
    const result = n1 * n2;

    return result
}



const div = (n1, n2) => {
    const result = n1 / n2;

    return result
}


module.exports = {
    soma,
    sub,
    mult,
    div
}




